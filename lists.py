# Example for append()
fruits = ["apple", "banana"]
fruits.append("orange")
print(fruits)  

# Example for extend()
numbers = [1, 2]
numbers.extend([3, 4])
print(numbers)  

# Example for insert()
colors = ["red", "blue"]
colors.insert(1, "green")
print(colors)  

# Example for remove()
animals = ["cat", "dog", "cat"]
animals.remove("cat")
print(animals)  

# Example for pop()
stack = [1, 2, 3]
last = stack.pop()
print(last, stack) 

# Example for clear()
items = [1, 2, 3]
items.clear()
print(items)  